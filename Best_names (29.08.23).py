# your first for loop: can you guess what it does?

names_i_like = ['Thomson', 'Thompson', 'Haddock', 'Snowy']

best_countries = ['India', 'United Kingdom', 'United States', 'Singapore']

for name in names_i_like:
    print(f"I like this name: {name}")

for country in best_countries:
    print(f"Among other countries, I like '{country}' the most! ")
